// C program to calculate the basic counting between A and B
#include <stdio.h>

int main() {
    // Write C code here
    int A, B;
    printf("Enter two numbers: ");
    scanf("%d %d",&A,&B);
    printf("A + B = %d",(A+B));
    printf("\nA - B = %d",(A-B));
    printf("\nA * B = %d",(A*B));
    printf("\nA / B = %d",(A/B));
    return 0;
}
